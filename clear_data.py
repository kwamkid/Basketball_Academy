def delete_collection(collection_name):
    """‡∏•‡∏ö‡∏ó‡∏∏‡∏Å document ‡πÉ‡∏ô collection"""
    docs = db.collection(collection_name).stream()
    deleted = 0

    for doc in docs:
        doc.reference.delete()
        deleted += 1
        if deleted % 10 == 0:
            print(f"   ‡∏•‡∏ö‡πÅ‡∏•‡πâ‡∏ß {deleted} ‡∏£‡∏≤‡∏¢‡∏Å‡∏≤‡∏£...")

